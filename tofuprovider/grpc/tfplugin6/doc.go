// Package tfplugin6 contains the automatically-generated protocol stubs for
// major version 6 of the gRPC-based provider protocol.
//
// This is exposed here because the protocol buffers library only allows one
// package in any program to contain descriptors for each source protobuf
// module and so any program that wishes to use the tofuproviders abstraction
// _and_ the lower-level protocol directly will need to access the lower-level
// protocol through this specific Go package to avoid protobuf namespace
// conflicts at init time.
